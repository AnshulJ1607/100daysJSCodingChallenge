// Using startsWith()

// program to check if a string starts with another string

const string = 'Anshul Jain';

const toCheStr = 'Rah';

const chekStr = 'Ansh'

if(string.startsWith(toCheStr)){
    console.log('The string Starts with Rah');
}
else {
    console.log('The string does not Starts with Rah')
}

if(string.startsWith(chekStr)){
    console.log('The string Starts with Ansh');
}
else {
    console.log('The string does not Starts with Ansh')
}